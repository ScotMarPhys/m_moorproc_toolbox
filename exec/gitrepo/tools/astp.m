function alpha = astp(S,T,P,SIGMAT)

%     COMPUTATION OF SIGMA-STP FROM TEMPERATURE
%     T(C), SALINITY (1/1000), PRESSURE (DBAR), AND SIGMA-T (KG/M.^3)
%
%     REFERENCE FOR SIGMA-STP CALCULATION-
%     WANG+MILLERO, J.G.R. 1974

      V =[1.8159725e-2,0.9998396e0,1.8224944e-2,-7.922210e-6,...
         -5.544846e-8,1.497562e-10,-3.932952e-13];
      B =[2.165928e-2,50.8863e0,0.7171582e0,0.7819867e-3,..
          3.162214e-5,-1.323594e-7,6.34575e-10,72.5116e0,-0.46352e0,...
          6.841e-3,-6.6586e-5,3.87310e-7,6.51e-2];
      C =[3.4737e0,-7.1486e-3,1.0250e-3,-3.9478e-5,2.9681e-7,...
          9.45e-3];
      D =[3.796e-5,-1.015e-6,-3.790e-7,2.044e-8,-3.190e-10,-4.57e-6];

      VW0=(1.+V(1).*T)./(V(2)+T.*(V(3)+T.*(V(4)+T...
     .^(V(5)+T.*(V(6)+T.*V(7))))));
      BW0=(1.+B(1).*T)./(B(2)+T.*(B(3)+T.*(B(4)+T...
     .*(B(5)+T.*B(6)+T.*B(7)))));
        S35=S-35.;
      PBAR=0.1.*P;
      BSW=BW0.*1.0e+6+S.*(B(8)+T.*(B(9)+T.*(B(10)+T...
     .*(B(11)+T.* B(12)))))+B(13).*S35;
      A1=C(1)+T.*(C(2)+T.*(C(3)+T.*(C(4)+T.*C(5))))+C(6).*S35;
        A2=D(1)+T.*(D(2)+T.*(D(3)+T.*(D(4)+T.*D(5))))+D(6).*S35;
      VSW0=1.000028e+0./(1.0e0+1.0e-3.*SIGMAT);
      alpha=VSW0-PBAR.*VW0./(BSW+PBAR.*(A1+PBAR.*A2));
